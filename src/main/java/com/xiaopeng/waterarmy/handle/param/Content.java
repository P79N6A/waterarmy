package com.xiaopeng.waterarmy.handle.param;

public class Content {

    /**
     * 文本内容
     */
    private String text;

    /**
     * 标题
     */
    private String title;


    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
